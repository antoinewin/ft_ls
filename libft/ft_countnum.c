#include "libft.h"

int		ft_countnum(int n)
{
	int		c;

	c = 0;
	if (n <= 9)
		c = 1;
	if (n >= 10)
	{
		while (n > 0)
		{
			n = n / 10;
			c++;
		}
	}
	return (c);
}
